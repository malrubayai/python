# This circle module has functions that performs
# calculates related to circles.
import math

# The area functions accepts a circle's radius as an
# argument and return the area of the circle.
def area(radius):
    return math.pi * radius**2

# The circumference function accepts a circle's
# function and returns the circles's circumference.
def circumference(radius):
    return 2 * math.pi * radius

